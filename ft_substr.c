#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*dest;
	size_t	i;

	i = 0;
	dest = (char *)malloc(len + 1);
	if (s == 0 || dest == 0)
		return (0);
	if (start >= ft_strlen(s))
		return ("");
	while (i < len && s[start + i])
	{
		dest[i] = s[start + i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
